# As redes neurais artificiais são sistemas inspirados no cérebro humano.
# Elas aprendem a reconhecer padrões e tomar decisões a partir de exemplos, assim como nós aprendemos com a experiência.

# Essas redes são formadas por “neurônios” artificiais que recebem informações, fazem cálculos e passam o resultado para outros neurônios. Quando organizadas em várias camadas, conseguem entender coisas mais complexas, como identificar uma roupa em uma foto ou reconhecer uma voz.

# O Deep Learning (ou aprendizado profundo) é uma forma mais avançada de rede neural, com muitas camadas internas. Isso permite que o computador aprenda sozinho os detalhes mais importantes dos dados, sem precisar que alguém diga o que observar.


# Um sistema de deep learning pode ser treinado para reconhecer números escritos à mão. Ele vê milhares de exemplos de cada número e, depois de aprender, consegue identificar sozinho se uma nova imagem mostra um “3”, um “7” ou outro dígito qualquer.